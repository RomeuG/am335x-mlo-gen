use clap::Parser;
use std::fs::File;
use std::io::Write;
use std::time::{SystemTime, UNIX_EPOCH};

type Result<T> = std::result::Result<T, Error>;

#[derive(Debug)]
pub enum Error {
    FileCreate(std::io::Error),
    FileOpen(std::io::Error),

    WriteError(std::io::Error),

    FsCopyError,
    FsMetadata,

    BufRead(std::io::Error),

    Seek(u64),
}

#[derive(Debug, Parser)]
#[clap(author, version, about, long_about = None)]
struct Args {
    /// Input file
    #[clap(short, long)]
    input: String,

    /// Load address
    #[clap(short, long, default_value_t = 0x402F0400)]
    address: i32,

    /// Output file
    #[clap(short, long, default_value = "MLO")]
    output: String,
}

fn prng() -> u64 {
    // get time for the seed
    let now = SystemTime::now();
    let timestamp = now.duration_since(UNIX_EPOCH).unwrap();
    let timestamp_ms = timestamp.as_secs();

    let a = 1103515245;
    let c = 12345;
    let m = 2147483647;

    (a * timestamp_ms + c) % m
}

